const Database = require('better-sqlite3');
const { v4: uuidv4 } = require('uuid');
require('dotenv').config();

const db = new Database(process.env.DB_PATH || './data/simulator.db');
db.pragma('foreign_keys = ON');

const existing = db.prepare("SELECT COUNT(*) as c FROM letters").get();
if(existing.c > 0){
  console.log('Листи вже в базі: '+existing.c+'. Видаліть спочатку якщо хочете переініціалізувати.');
  console.log('Команда: node -e "require(\'better-sqlite3\')(process.env.DB_PATH||\'./data/simulator.db\').exec(\'DELETE FROM letters\')"');
  process.exit(0);
}

// Ensure new columns exist
const cols = db.prepare("PRAGMA table_info(letters)").all().map(c=>c.name);
const newCols = [
  ['difficulty',"TEXT DEFAULT 'СЕРЕДНЄ'"],
  ['task_hint','TEXT'],
  ['has_photo','INTEGER DEFAULT 0'],
  ['photo_file','TEXT'],
  ['contract_number','TEXT'],
  ['per_tonne_rate','REAL'],
  ['incoterms','TEXT DEFAULT \'FCA\''],
  ['scenario','TEXT DEFAULT \'agree\''],
  ['attachment_type','TEXT'],
];
for(const [col,def] of newCols){
  if(!cols.includes(col)){
    try{ db.exec('ALTER TABLE letters ADD COLUMN '+col+' '+def); }catch(e){}
  }
}

const ins = db.prepare(`
  INSERT INTO letters (
    id,code,type,country,from_name,company,email_addr,subject,body,
    missing,vehicle,dirs,
    freight_fixed,freight_amount,freight_min,freight_max,
    carrier_range_min,carrier_range_max,distance,
    load_day_offset,deliv_day_offset,active,
    difficulty,task_hint,has_photo,photo_file,
    contract_number,incoterms,scenario,attachment_type
  ) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,1,?,?,?,?,?,?,?,?)
`);

const insertAll = db.transaction(arr=>{
  for(const l of arr){
    ins.run(
      uuidv4(),l.code,l.type,l.country,l.from_name,l.company,l.email_addr,
      l.subject,l.body,JSON.stringify(l.missing),l.vehicle,JSON.stringify(l.dirs),
      l.freight_fixed?1:0, l.freight_amount, l.freight_min, l.freight_max,
      l.c_min, l.c_max, l.distance,
      l.load_day_offset, l.deliv_day_offset,
      l.difficulty, l.task_hint,
      l.has_photo?1:0, l.photo_file,
      l.contract_number, l.incoterms, l.scenario, l.attachment_type
    );
  }
});

