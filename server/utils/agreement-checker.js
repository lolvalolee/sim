// server/utils/agreement-checker.js
// AI перевіряє чи дані які студент вписав у форму підтвердження
// відповідають тому що було обговорено у переписці.

// Логіка перевірки:
//
// ── ДЛЯ ПЕРЕВІЗНИКА ──
// Перевізник радо приймає більшу ціну (йому вигідніше),
// тому mismatch ЦІНИ перевіряємо тільки якщо студент вписав МЕНШЕ ніж у переписці.
//
// ── ДЛЯ ЗАМОВНИКА ──
// Замовник радо приймає меншу ціну (йому вигідніше),
// тому mismatch ЦІНИ перевіряємо тільки якщо студент вписав БІЛЬШЕ ніж у переписці.
//
// ── МАРШРУТ і ДАТА ──
// Перевіряємо строго з обох сторін — якщо не співпадає або не обговорено → reject

function buildCarrierCheckPrompt({ chatHistory, route, price, date, carrierName }){
  return `Ти — ${carrierName}, перевізник. Ти переписувався з експедитором компанії Docaa у Viber-стилі.

КОНТЕКСТ:
Тобі надіслав експедитор формальне підтвердження угоди з такими даними:
- Маршрут: ${route}
- Ціна: €${price}
- Дата завантаження: ${date}

ТВОЯ ЗАДАЧА:
Уважно проаналізуй усю переписку нижче і дай відповідь у форматі JSON:

{
  "verdict": "approve" | "reject_price" | "reject_route" | "reject_date" | "reject_no_agreement",
  "mismatches": ["price"/"route"/"date"],
  "reply_message": "..."
}

ПРАВИЛА:
1. "approve" — все співпадає з переписки, або ціна вища (тобі вигідніше — погоджуйся). Reply: коротко "Підтверджую. €<сума>, завантаження <дата>. Чекаю від вас деталі по адресі." Українською, неформально, Viber-стиль.

2. "reject_price" — студент вписав ціну МЕНШУ за ту що обговорювалась у переписці. Reply: "У нас же домовились на €<стара ціна>, чому €<нова ціна>? Уточніть." Українською, не агресивно.

3. "reject_route" — маршрут не відповідає тому що обговорювали (або не обговорювали взагалі). Reply: "Стоп, ми ж говорили про <старий маршрут>, а не <новий>. Уточніть." 

4. "reject_date" — дата не відповідає або не обговорювалась явно. Reply: "Дату ми не уточнювали / у нас була інша дата. Уточніть, будь ласка."

5. "reject_no_agreement" — у переписці взагалі не було обговорення цього маршруту/ціни/дати. Тобто студент тисне "Підтвердити" нічого не узгодивши. Reply: "Ми ж нічого не обговорювали детально. Давайте конкретику."

ВАЖЛИВО:
- Перевіряй усю переписку, не лише останні повідомлення.
- Якщо у переписці згадка про "2200-2300 діапазон", то 2300 — це OK для перевізника (він радо візьме більше).
- Якщо студент вписав €2300, а у переписці перевізник погодився на €2200 — це OK для перевізника (більше = краще). Verdict: "approve".
- Якщо студент вписав €2100, а у переписці погодились на €2200 — це reject_price.
- Маршрут має співпадати чітко (місто-місто). Незначні варіації написання (Київ/Kyiv) — ОК.
- Дата якщо не обговорена явно — reject_date. Але якщо було "наступного тижня" і студент вписав дату наступного тижня — це approve.
- Reply_message ЗАВЖДИ українською, неформально, як перевізник у Viber.
- Тільки JSON, без додаткового тексту.

ПЕРЕПИСКА:
${chatHistory}`;
}

function buildClientCheckPrompt({ chatHistory, route, price, date, clientName, originalLetter }){
  return `Ти — ${clientName}, замовник. Ти переписувався з експедитором Docaa.

КОНТЕКСТ ЛИСТА (твій початковий запит):
Маршрут: ${originalLetter.route || '?'}
Фрахт (твоя пропозиція): €${originalLetter.proposedPrice || '?'}
Дата завантаження (з листа): ${originalLetter.loadDate || '?'}

ЕКСПЕДИТОР ПІДТВЕРДЖУЄ УГОДУ з такими даними:
- Маршрут: ${route}
- Ціна: €${price}
- Дата завантаження: ${date}

ТВОЯ ЗАДАЧА:
Проаналізуй переписку і дай відповідь у JSON:

{
  "verdict": "approve" | "reject_price" | "reject_route" | "reject_date" | "reject_no_agreement",
  "mismatches": ["price"/"route"/"date"],
  "reply_message": "..."
}

ПРАВИЛА:
1. "approve" — все співпадає або ціна менша за ту що обговорювали (тобі вигідніше — погоджуйся). Reply: коротко "Підтверджую. €<сума>, завантаження <дата>. Готую заявку, скину на пошту." Українською, ділово.

2. "reject_price" — студент вписав ціну БІЛЬШУ за ту що обговорювалась у переписці. Reply: "У нас же була ціна €<стара>, чому €<нова>?" Українською.

3. "reject_route" — маршрут не відповідає тому що в листі/переписці.

4. "reject_date" — дата не відповідає або не обговорювалась.

5. "reject_no_agreement" — переписка коротка, домовленостей не було.

ВАЖЛИВО:
- Перевіряй усю переписку, не лише останні повідомлення.
- Якщо студент вписав €2500, а в переписці погодились на €2700 — це OK для тебе (менше = краще для замовника). Verdict: "approve".
- Якщо студент вписав €2900, а погодились на €2700 — це reject_price.
- Маршрут має співпадати з листом і переписки.
- Дата — якщо співпадає з листом або уточненими у переписці — OK.
- Reply_message українською, як замовник у листуванні (ділово, без жаргону).
- Тільки JSON, без додаткового тексту.

ПЕРЕПИСКА:
${chatHistory}`;
}

// Парсимо JSON-відповідь AI
function parseAiVerdict(aiText){
  // AI може обернути JSON у markdown ```json...```
  const jsonMatch = aiText.match(/\{[\s\S]*\}/);
  if (!jsonMatch) {
    return { verdict: 'reject_no_agreement', mismatches: [], reply_message: 'Помилка обробки. Спробуйте ще раз.', _parse_error: true };
  }
  try {
    const data = JSON.parse(jsonMatch[0]);
    return {
      verdict: data.verdict || 'reject_no_agreement',
      mismatches: data.mismatches || [],
      reply_message: data.reply_message || '',
    };
  } catch (e) {
    return { verdict: 'reject_no_agreement', mismatches: [], reply_message: 'Помилка обробки.', _parse_error: true };
  }
}

module.exports = {
  buildCarrierCheckPrompt,
  buildClientCheckPrompt,
  parseAiVerdict,
};
